package JAVA.Fundamentals;
class abc4 {
    public static void main(String[] args) {
        int number = 10;

        // if statement
        if (number > 0) {
            System.out.println("The number is positive.");
        }
        // else if statement
        else if (number == 0) {
            System.out.println("The number is zero.");
        }
        // else statement
        else {
            System.out.println("The number is negative.");
        }
    }
}